async def seed_catalog(db):
    """Additive seed — inserts only products that don't already exist (by id), so admin CRUD edits persist."""
    await db.products.create_index("id", unique=True)
    await db.products.create_index("slug")
    await db.products.create_index("category")
    existing_ids = {doc["id"] async for doc in db.products.find({}, {"id": 1, "_id": 0})}
    for p in SEED_PRODUCTS:
        if p["id"] not in existing_ids:
            await db.products.insert_one({**p})
    if await db.collections.count_documents({}) == 0:
        for c in COLLECTIONS:
            await db.collections.insert_one({**c})


@router.get("")
async def list_products(
    category: Optional[str] = None,
    is_new: Optional[bool] = None,
    on_sale: Optional[bool] = None,
    summer: Optional[bool] = None,
    sort: Optional[str] = Query(None, regex="^(price_asc|price_desc|newest|featured)$"),
    search: Optional[str] = None,
    min_price: Optional[float] = None,
    max_price: Optional[float] = None,
    limit: int = 200,
):
    from server import db
    query: dict = {}
    if category and category != "All":
        query["category"] = category
    if is_new is not None:
        query["is_new"] = is_new
    if on_sale is not None:
        query["on_sale"] = on_sale
    if summer:
        query["id"] = {"$in": list(SUMMER_IDS)}
    if search:
        query["$or"] = [
            {"name": {"$regex": search, "$options": "i"}},
            {"tags": {"$regex": search, "$options": "i"}},
        ]
    if min_price is not None or max_price is not None:
        pr: dict = {}
        if min_price is not None:
            pr["$gte"] = min_price
        if max_price is not None:
            pr["$lte"] = max_price
        query["price"] = pr

    cursor = db.products.find(query, {"_id": 0})
    if sort == "price_asc":
        cursor = cursor.sort("price", 1)
    elif sort == "price_desc":
        cursor = cursor.sort("price", -1)
    elif sort == "newest":
        cursor = cursor.sort("is_new", -1)
    items = await cursor.to_list(limit)
    return {"items": items, "total": len(items)}


